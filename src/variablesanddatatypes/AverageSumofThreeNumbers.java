package variablesanddatatypes;

public class AverageSumofThreeNumbers {

	public static void main(String[] args) {
		int x=10;
		int y=20;
		int z=30;
		int total=x+y+z;
		int average=total/3;
		System.out.println("The Total of 3 numbers="+total);
		System.out.println("The average of 3 numbers="+average);

	}

}
